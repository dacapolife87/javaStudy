package week03.examplesrc;

public class SwitchExample {
    public static void main(String[] args) {
        int a = 10;
        switch (a){
            case 10 :
                System.out.println("10");
                break;
            case 20 :
                System.out.println("20");
            default:
                System.out.println("default");
        }
    }
}
