package week03.examplesrc;

public class Comp {
    public static void main(String[] args) {
        int a = 10;
        int b = 9;

        System.out.println("a : " + a + ", b : " + b);
        System.out.println("a < b : " + (a < b));
        System.out.println("a > b : " + (a > b));

        int c = 10;
        int d = 10;
        System.out.println("c : " + c + ", d : " + d);
        System.out.println("c <= d : " + (c <= d));
        System.out.println("c >= d : " + (c >= d));
    }
}
